def find_relevant_knowledge(message):
    if not client:
        return None
    try:
        # ‡¶ó‡ßç‡¶∞‡¶æ‡¶π‡¶ï‡ßá‡¶∞ ‡¶Æ‡ßá‡¶∏‡ßá‡¶ú ‡¶•‡ßá‡¶ï‡ßá ‡¶Ö‡¶∞‡ßç‡¶•‡¶¨‡¶π ‡¶∂‡¶¨‡ßç‡¶¶‡¶ó‡ßÅ‡¶≤‡ßã ‡¶¨‡ßá‡¶∞ ‡¶ï‡¶∞‡¶æ
        words = re.split(r'\s|[,.?]', message.lower())
        meaningful_words = [word for word in words if word]
        if not meaningful_words:
            return None
            
        # ‡¶è‡¶ï‡¶ü‡¶ø Regex ‡¶™‡ßç‡¶Ø‡¶æ‡¶ü‡¶æ‡¶∞‡ßç‡¶® ‡¶§‡ßà‡¶∞‡¶ø ‡¶ï‡¶∞‡¶æ ‡¶Ø‡¶æ ‡¶Ø‡ßá‡¶ï‡ßã‡¶®‡ßã ‡¶è‡¶ï‡¶ü‡¶ø ‡¶∂‡¶¨‡ßç‡¶¶ ‡¶ñ‡ßÅ‡¶Å‡¶ú‡¶¨‡ßá
        # ‡¶â‡¶¶‡¶æ‡¶π‡¶∞‡¶£: "chicken roll dam" -> "chicken|roll|dam"
        regex_pattern = "|".join(meaningful_words)
        regex_query = re.compile(regex_pattern, re.IGNORECASE)

        # ‡¶°‡ßá‡¶ü‡¶æ‡¶¨‡ßá‡¶∏‡ßá ‡¶è‡¶Æ‡¶® ‡¶°‡¶ï‡ßÅ‡¶Æ‡ßá‡¶®‡ßç‡¶ü ‡¶ñ‡ßã‡¶Å‡¶ú‡¶æ ‡¶π‡¶ö‡ßç‡¶õ‡ßá ‡¶Ø‡¶æ‡¶∞ keywords ‡¶§‡¶æ‡¶≤‡¶ø‡¶ï‡¶æ‡¶∞ ‡¶ï‡ßã‡¶®‡ßã ‡¶è‡¶ï‡¶ü‡¶ø ‡¶∂‡¶¨‡ßç‡¶¶ ‡¶è‡¶á ‡¶™‡ßç‡¶Ø‡¶æ‡¶ü‡¶æ‡¶∞‡ßç‡¶®‡ßá‡¶∞ ‡¶∏‡¶æ‡¶•‡ßá ‡¶Æ‡ßá‡¶≤‡ßá
        knowledge_cursor = knowledge_collection.find({"keywords": {"$regex": regex_query}})
        
        information_list = [doc.get("information") for doc in knowledge_cursor if doc.get("information")]
        
        if information_list:
            print(f"‡¶°‡ßá‡¶ü‡¶æ‡¶¨‡ßá‡¶∏ ‡¶•‡ßá‡¶ï‡ßá {len(information_list)} ‡¶ü‡¶ø ‡¶™‡ßç‡¶∞‡¶æ‡¶∏‡¶ô‡ßç‡¶ó‡¶ø‡¶ï ‡¶§‡¶•‡ßç‡¶Ø ‡¶™‡¶æ‡¶ì‡ßü‡¶æ ‡¶ó‡ßá‡¶õ‡ßá (RAG)‡•§")
            return "\n".join(information_list)
            
    except Exception as e:
        print(f"‡¶°‡ßá‡¶ü‡¶æ‡¶¨‡ßá‡¶∏ ‡¶•‡ßá‡¶ï‡ßá ‡¶ú‡ßç‡¶û‡¶æ‡¶® ‡¶ñ‡ßÅ‡¶Å‡¶ú‡¶§‡ßá ‡¶∏‡¶Æ‡¶∏‡ßç‡¶Ø‡¶æ: {e}")
        
